package day03_Variables;

/*
\n: new line (Enter)
\t: paragraph space
\": double quote
\\: back slash
 */

public class Escape_Sequences {

    public static void main(String[] args) {

        System.out.println("Hello everyone \nHow are you all today?");
        System.out.println("----------------------------------------");


        System.out.println("Hello everyone\nMy name is Bekzod \nI'm from Tashkent");
        System.out.println("----------------------------------------");


        System.out.println("\tToday is a great day to study Java");
        System.out.println("----------------------------------------");

        System.out.println("My favourite TV series is \"Game of Thrones\"");

        System.out.println("/");
        System.out.println("\\");
        System.out.println("---------------------");

        System.out.println("     ^     ");
        System.out.println("    / \\    ");
        System.out.println("   /   \\    ");
        System.out.println("  /     \\    ");
        System.out.println(" /       \\    ");
        System.out.println(" ---------");
        System.out.println("-------------------------------");

        System.out.println("'Java'");
    }

}
