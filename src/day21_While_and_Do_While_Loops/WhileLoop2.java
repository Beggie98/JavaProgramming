package day21_While_and_Do_While_Loops;

public class WhileLoop2 {
    public static void main(String[] args) {
        for (int i=0; i<3;i++){
            System.out.println("Hello Cybertek");
        }

        System.out.println("-------------------------------------");

        int i = 0;
        while (i<3){
            System.out.println("Hello World");
            i++;
        }
    }
}
