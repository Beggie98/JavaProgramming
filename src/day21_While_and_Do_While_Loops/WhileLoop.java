package day21_While_and_Do_While_Loops;

public class WhileLoop {
    public static void main(String[] args) {

        if (true){
            System.out.println("Hello World");
        }
        System.out.println("-----------------------------");

        //while loop: repeated if statement



        while (true){
            System.out.println("Hello World");
        }

    }
}
