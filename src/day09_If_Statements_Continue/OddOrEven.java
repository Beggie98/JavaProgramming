package day09_If_Statements_Continue;

public class OddOrEven {

    public static void main(String[] args) {

        int num = 45;

        if(num % 2 == 0){
            System.out.println(num + " is an even number");
        }else {
            System.out.println(num + " is an odd number");
        }




    }

}
